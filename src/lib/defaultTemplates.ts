export async function forceUpdateTemplates() {
    await prisma.messagingTemplate.deleteMany({});

    for (const t of DEFAULT_TEMPLATES) {
        await prisma.messagingTemplate.create({
            data: {
                ...t,
                type: "WHATSAPP"
            }
        });
    }
}
